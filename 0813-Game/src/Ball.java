
public class Ball {
	int posX;	
	int posY;
	int speedX;
	int speedY;
	int width;
	int height;
	
	public Ball(int posX, int posY, int speedX, int speedY, int width, int height) {
		this.posX = posX;
		this.posY = posY;
		this.speedX = speedX;
		this.speedY = speedY;
		this.width = width;
		this.height = height;
	}
	
}
